    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $user)
    {
        $user->delete();
        return back()->with('users',User::all());
    }

    /**
     * Modfy the role oof the users
     */
    public function modify(User $user)
    {
        if($user->isadmin()){
            $user->role=0;
        }else{
            $user->role=1;
        }
        $user->save();
        return back()->with('users',User::all());
    }
